package cn.dolphinsoft.hilife.common.enumeration;

public enum MessageReadStatus {

    READ("1", "已读"), UNREAD("2", "未读");

    private String key;

    private String value;

    private MessageReadStatus(String key, String value) {
        this.key = key;
        this.value = value;
    }

    /**
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * @param key
     *            the key to set
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * @return the value
     */
    public String getValue() {
        return value;
    }

    /**
     * @param value
     *            the value to set
     */
    public void setValue(String value) {
        this.value = value;
    }

}
