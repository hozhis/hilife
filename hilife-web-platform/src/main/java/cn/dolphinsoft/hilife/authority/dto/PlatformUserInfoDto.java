package cn.dolphinsoft.hilife.authority.dto;

import cn.dolphinsoft.hilife.common.dto.RequestDto;

/**
 * Class Name: PlatformUserInfoDto
 * 
 * @author zhishenghong
 *
 */
public class PlatformUserInfoDto extends RequestDto {

    private static final long serialVersionUID = -6984622503817702397L;

    private Integer userId;

    private String loginId;

    private String userName;

    private Long phone;

    private String email;

    private String status;

    private String remark;

    private String oldPwd;

    private String newPwd;

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getLoginId() {
        return loginId;
    }

    public void setLoginId(String loginId) {
        this.loginId = loginId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public Long getPhone() {
        return phone;
    }

    public void setPhone(Long phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getNewPwd() {
        return newPwd;
    }

    public void setNewPwd(String newPwd) {
        this.newPwd = newPwd;
    }

    public String getOldPwd() {
        return oldPwd;
    }

    public void setOldPwd(String oldPwd) {
        this.oldPwd = oldPwd;
    }

}
